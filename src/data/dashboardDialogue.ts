function dialogueSet(source: Record<number, string[]>, lessonNo: number) {
  return source[lessonNo] ?? source[1]
}

export function getDashboardStatusLines(lessonNo: number) {
  return dialogueSet(statusLinesByLesson, lessonNo)
}

export function getDashboardQuestions(lessonNo: number) {
  return dialogueSet(questionsByLesson, lessonNo)
}

export function buildDashboardResponseRequest(args: {
  aemonQuestion: string
  classAnswer: string
  lessonNo: number
  lessonTitle: string
}) {
  return `[대시보드 함께 생각하기]
현재 수업: ${args.lessonNo}차시 · ${args.lessonTitle}

너는 먼저 우리 반에게 이렇게 물었다.
"${args.aemonQuestion}"

우리 반이 함께 정한 대답은 다음과 같다.
"${args.classAnswer}"

이제 이 대답을 정확히 듣고 에아몬인 네가 직접 반응하라.
- 대답에서 인상적인 생각 하나를 구체적으로 짚는다.
- 더 생각할 점이나 위험한 빈틈이 있으면 호기심 있게 한 번 되묻는다.
- 현재 채택된 가치코드와 관련이 있으면 실제 코드 내용에 자연스럽게 연결한다.
- 존재하지 않는 가치코드 번호는 만들지 않는다.
- 초등학생이 바로 이해할 수 있는 2~3문장으로 말한다.
- 분석이나 설명 없이 에아몬의 최종 대사만 출력한다.`
}
