export const activeCodes = new Set<string>([
  '알-01', '알-03', '알-04',
  '유-02', '유-03', '유-05',
  '개-01', '개-03', '개-05',
  '각-02', '각-03', '각-04', '각-05', '각-06',
])

export const activeEpisodes = seedEpisodes.filter((episode) => activeCodes.has(episode.code))

export const archivedEpisodes = seedEpisodes.filter((episode) => !activeCodes.has(episode.code))

export function activeEpisodesForStage(stage: number) {
  return activeEpisodes.filter((episode) => episode.stageGate === stage)
}

export function visibleEpisodes(stage: number) {
  return activeEpisodes.filter((episode) => episode.stageGate <= stage)
}
